package Pokemon;

public class Pikachu extends Pokemons {
    @Override
public void gender() {
    System.out.println("Gender: Male");
} 
@Override
public void type() {
    System.out.println("Type: Electric");
}
@Override
public void sound() {
    System.out.println("Voice: Pika pika! Pikachu!");
}
@Override
public void tail() {
    System.out.println("The pokemon has a tail.");
}
@Override
public void colors() {
    System.out.println("Colors: Yellow body with red cheeks and black in the edge of it's ears.");
}
}

